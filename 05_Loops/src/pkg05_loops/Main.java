package pkg05_loops;

import java.util.Scanner;

/**
 *
 * @author kubin
 */
public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int number;
        while((number = sc.nextInt()) > 0){
            System.out.println(number);
        }
    }
    
}
